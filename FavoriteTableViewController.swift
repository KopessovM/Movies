import UIKit

class FavoriteTableViewController: UITableViewController {

    var favMovies: [String] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()
        title = "Favorites"
        navigationController?.navigationBar.tintColor = .black
        tableView.register(FavoriteTableViewCell.self, forCellReuseIdentifier: "mycell")
        favMovies = UserDefaults.standard.stringArray(forKey: "Movie") ?? []
        tableView.reloadData()
    }
    
    func removeDuplicates(array: [String]) -> [String] {
            var result = [String]()

            for value in array {
                if result.contains(value) == false {
                    result.append(value)
                }
            }
        return result
    }

    // MARK: - Table view data source
    
    override func numberOfSections(in tableView: UITableView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return removeDuplicates(array: favMovies).count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "mycell", for: indexPath) as! FavoriteTableViewCell
        let favorites = removeDuplicates(array: favMovies)
        cell.titleLabel.text = favorites[indexPath.row]
        return cell
    }
    
    override func tableView(_ tableView: UITableView, commit editingStyle: UITableViewCell.EditingStyle, forRowAt indexPath: IndexPath) {
        if editingStyle == .delete{
            tableView.beginUpdates()
            favMovies.remove(at: indexPath.row)
            UserDefaults.standard.set(favMovies, forKey: "Movie")
            tableView.deleteRows(at: [indexPath], with: .fade)
            tableView.endUpdates()
        }
    }
    
    override func tableView(_ tableView: UITableView, editingStyleForRowAt indexPath: IndexPath) -> UITableViewCell.EditingStyle {
        
        return .delete
    }

